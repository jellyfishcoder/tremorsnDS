// MARK: Increment the time
void incrementTime() {
	if(inGameTime == 4294967294) {
		inGameTime = 0;
	} else {
		inGameTime++;
	}
	updateGameTime();
}

// MARK: Update "sun" position
void updateGameTime() {
	if((inGameTime >= 0) && (inGameTime <= 2147483647)) {
		int mapAngle = pi*(inGameTime/2147483647);
		float mapAngleCos = -1.0f * cos(mapAngle);
		float mapAngleSin = -1.0f * sin(mapAngle);
		// Update the sun light
		glLight(0,
			RGB15(31,31,31),	// 31 is max with 5 bits
			floattov10(0.0f),	// X component is always 0
			floattov10(-1.0f * mapAngleCos),
			floattov10(-1.0f * mapAngleSin));
	} else {
		glLight(0,
			RGB15(0,0,0),
			floattov10(0.0f),floattov10(0.0f),floattov10(0.0f));
	}
}

// MARK: Get pressed key from name keyboard into char nameEntry[16]
void keyPress(int c) {
	bool toBreak = false;
	switch(c) {
		case 0:
			// 0 is ascii null char
			toBreak = true;
			break;
		case 8:
			// 8 is ascii backspace char, so delete
			nameEntryCurLen--;
			nameEntry[nameEntryCurLen] = NULL;
			toBreak = true;
			break;
		case 127:
			// 127 is ascii delete char, so delete
			nameEntryCurLen--;
			nameEntry[nameEntryCurLen] = NULL;
			toBreak = true;
			break;
		case 133:
			// 133 is ascii carriage return, so submit
			nameEntryDone = true;
			toBreak = true;
			break;
	}
	if((toBreak == false) && (nameEntryCurLen <= NAME_ENTRY_MAX_LEN)) {
		nameEntry[nameEntryCurLen] = c;
		nameEntryCurLen++;
	} else if (toBreak == false) {
		nameEntryCurLen--;
		nameEntry[nameEntryCurLen-1] = c;
	}
}

